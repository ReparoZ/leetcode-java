package problems.algorithms.A0021_MergeTwoSortedLists;

/**
 * @author reparo
 * @date 2021/11/20
 */
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}